void print_array(int array[30][30], int rows, int cols){
    int i, j;

    // print matrix of defined len by user
    printf("The entered matrix is:\n");
    for(i = 0; i < rows; i++ ){
        for(j = 0; j < cols; j++){
            printf( "%d ", array[i][j]);
        }
        printf("\n");
    }
}

void print_under(int array[30][30], int rows, int cols){
    int i, j;
    // init counting values for diagonal (subtract one as index starts with 0)
    int xcounter = cols-1;
    int ycounter = rows-1;

    printf("Under the secondary diagonal:\n");
    // loop through whole array
    for(i = 0; i < rows; i++ ){
        for(j = 0; j < cols; j++){
            // check if under diagonal
            if (i > xcounter && j > ycounter ){
                // if so, print
                printf("%d ", array[i][j]);
            }
            // control diagonal x
            xcounter--;
        }
        // control diagonal y
        ycounter--;
    }
    printf("\n");
}

